package gdv.xport.srv.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import springfox.documentation.builders.PathSelectors;
import springfox.documentation.builders.RequestHandlerSelectors;
import springfox.documentation.spi.DocumentationType;
import springfox.documentation.spring.web.plugins.Docket;
import springfox.documentation.swagger2.annotations.EnableSwagger2;

/**
 * Ueber diese Klasse wird Swagger aktiviert. Die Doku dazu stammt von
 * <a href="http://www.baeldung.com/swagger-2-documentation-for-spring-rest-api">Setting Up Swagger 2</a>.
 *
 * @author oboehm
 * @since 3.0 (20.02.2017)
 */
@Configuration
@EnableSwagger2
public class SwaggerConfig {

    @Bean
    public Docket api() {
        return new Docket(DocumentationType.SWAGGER_2)
                .select()
                .apis(RequestHandlerSelectors.any())
                .paths(PathSelectors.regex("/(api|info|health).*"))
                .build();
    }

}
